def build_handle_detail(handle: str, txs_for_handle: list[dict]) -> str:
    """Handle page — sent / received lists + simple totals."""
    sent = [t for t in txs_for_handle if t["from_handle"] == handle]
    received = [t for t in txs_for_handle if t["to_handle"] == handle and t["from_handle"] != handle]

    total_sent = sum(t["amount_seconds"] for t in sent)
    total_received = sum(t["amount_seconds"] for t in received)

    # Aggregate blue/red across all transfers (sent — that's where the
    # receiver assigned the rating; received doesn't carry this handle's
    # own rating).
    rated = sent  # sender's transactions carry the rating recipient gave
    if rated:
        avg_blue_int = round(sum(t.get("blue_pct", 100) for t in rated) / len(rated))
    else:
        avg_blue_int = 0
    avg_red_int = 100 - avg_blue_int
    has_rating = bool(rated)

    def tx_table(rows: list[dict], empty_msg: str) -> str:
        if not rows:
            return f"<p style=\"color:var(--muted)\">{empty_msg}</p>"
        body = "\n".join(_tx_row(t) for t in rows)
        return f"""<div class="tx-table-wrap">
<table class="tx-table">
<thead><tr><th>Time (UTC)</th><th>From</th><th>To</th><th>Amount</th><th>Blue / Red</th><th>Node</th></tr></thead>
<tbody>{body}</tbody>
</table>
</div>"""

    if has_rating:
        br_card_body = (
            f'<div style="display:flex;align-items:center;gap:0.6rem;margin-top:0.35rem">'
            f'<span class="br-bar br-bar-lg" aria-label="{avg_blue_int}% blue, {avg_red_int}% red">'
            f'<span class="br-fill br-blue" style="width:{avg_blue_int}%"></span>'
            f'<span class="br-fill br-red"  style="width:{avg_red_int}%"></span>'
            f'</span>'
            f'<span class="br-num">{avg_blue_int}%</span>'
            f'</div>'
        )
    else:
        br_card_body = '<div class="value" style="font-size:1.1rem;color:var(--muted)">n/a</div>'

    stats = f"""<div class="stats-grid">
  <div class="stat-card">
    <div class="label">Total received</div>
    <div class="value">{fmt_short(total_received)}</div>
    <div class="sub">{len(received)} transfer{"s" if len(received)!=1 else ""}</div>
  </div>
  <div class="stat-card">
    <div class="label">Total sent</div>
    <div class="value">{fmt_short(total_sent)}</div>
    <div class="sub">{len(sent)} transfer{"s" if len(sent)!=1 else ""}</div>
  </div>
  <div class="stat-card">
    <div class="label">Blue / Red (outgoing)</div>
    {br_card_body}
    <div class="sub">how recipients rated this handle</div>
  </div>
</div>"""

    body = f"""<p class="tagline">Handle</p>
<h1><span style="font-family: 'SF Mono', Menlo, Consolas, monospace; color:var(--accent)">{html.escape(handle)}</span></h1>
<p>Public history of every transaction this handle is party to. Privacy is by design 100% public — every handle's full history is visible without opt-in.</p>

<h2>At a glance</h2>
{stats}

<h2>Received <span style="font-weight:400;font-size:1rem;color:var(--muted)">({len(received)})</span></h2>
{tx_table(received, "No incoming transfers yet.")}

<h2>Sent <span style="font-weight:400;font-size:1rem;color:var(--muted)">({len(sent)})</span></h2>
{tx_table(sent, "No outgoing transfers yet.")}
"""
    return _page_shell(f"{handle} — ubi.world", body, active="handle", topbar_label="Handle")



