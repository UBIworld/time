# --------------------------------------------------------------------------- #
# Atomic write helper
# --------------------------------------------------------------------------- #

def _atomic_write(path: Path, content: str) -> bool:
    """Write `content` to `path`, only touching the file if bytes differ.

    Returns True if the file was (re)written. This keeps the generator
    idempotent and avoids needlessly bumping mtimes (which would
    confuse Cloudflare's origin freshness checks).
    """
    new_bytes = content.encode("utf-8")
    if path.exists():
        try:
            if path.read_bytes() == new_bytes:
                return False
        except OSError:
            pass
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp = path.with_suffix(path.suffix + ".tmp")
    tmp.write_bytes(new_bytes)
    os.replace(tmp, path)
    return True


