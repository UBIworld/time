def generate(ledger_root: Path) -> dict:
    """Re-render all pages. `ledger_root` is the directory that contains
    `transactions/`; the output goes to its parent."""
    out_root = ledger_root.parent
    tx_dir = ledger_root / "transactions"

    txs = load_transactions(tx_dir)
    by_handle = index_by_handle(txs)
    all_handles = list(by_handle.keys())

    counts = {"home": 0, "tx": 0, "handle": 0, "style": 0, "browse": 0, "txs_loaded": len(txs)}

    # style.css
    if _atomic_write(out_root / "style.css", STYLE_CSS.lstrip()):
        counts["style"] = 1

    # Home
    if _atomic_write(out_root / "index.html", build_home(txs, all_handles)):
        counts["home"] = 1

    # Transaction detail pages
    tx_out = out_root / "tx"
    for tx in txs:
        if _atomic_write(tx_out / f"{tx['id']}.html", build_tx_detail(tx)):
            counts["tx"] += 1

    # Handle detail pages — raw `:` on disk (Apache decodes %3A before
    # looking up the file, so encoded filenames 404). Links in the
    # generated HTML are URL-encoded so the *URLs* stay RFC-clean.
    handle_out = out_root / "handle"
    for handle, handle_txs in by_handle.items():
        if _atomic_write(handle_out / handle_filename(handle), build_handle_detail(handle, handle_txs)):
            counts["handle"] += 1

    # /ledger/ browse pages — replace the old Google-Fonts island with the
    # family top-bar shell so they match the rest of the site.
    counts["browse"] = 0
    if _atomic_write(ledger_root / "index.html", build_ledger_root_browse()):
        counts["browse"] += 1
    if _atomic_write(ledger_root / "transactions" / "index.html", build_ledger_transactions_browse()):
        counts["browse"] += 1
    if _atomic_write(ledger_root / "nodes" / "index.html", build_ledger_nodes_browse()):
        counts["browse"] += 1

    return counts


def main(argv=None) -> int:
    parser = argparse.ArgumentParser(description="ubi.world static-site generator")
    parser.add_argument("--ledger-root", default=None, help="root of the ledger (parent of transactions/)")
    parser.add_argument("--verbose", "-v", action="store_true")
    args = parser.parse_args(argv)

    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.INFO,
        format="%(asctime)s  %(levelname)s  %(message)s",
    )

    root = args.ledger_root or os.environ.get("LEDGER_ROOT") or (getattr(config, "LEDGER_ROOT", None) if config else None)
    if not root:
        print("error: --ledger-root not given and LEDGER_ROOT not set", file=sys.stderr)
        return 2

    root_path = Path(root).expanduser()
    if not root_path.exists():
        print(f"error: ledger root does not exist: {root_path}", file=sys.stderr)
        return 2

    counts = generate(root_path)
    logger.info("generated: %s", counts)
    return 0


if __name__ == "__main__":
    sys.exit(main())
